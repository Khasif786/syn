from syn.five import NUM

#-------------------------------------------------------------------------------
# Comparison

def feq(a, b, tol=1e-8):
    if isinstance(a, NUM) and isinstance(b, NUM):
        ret = abs(abs(a) - abs(b)) < tol
        return ret
    return a == b

#-------------------------------------------------------------------------------
# __all__

__all__ = ('feq',)

